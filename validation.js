function setFieldResult(form, fieldName, isValid, message) {
  if (isValid) {
    clearFieldError(form, fieldName);
    return true;
  }

  const errorElement = form.querySelector(`#${fieldName}-error`);
  const field = form.elements[fieldName];

  if (errorElement) {
    errorElement.textContent = message;
    errorElement.classList.remove("hidden");
    errorElement.setAttribute("role", "alert");
  }

  if (field instanceof RadioNodeList) {
    const radios = form.querySelectorAll(`input[name="${fieldName}"]`);
    radios.forEach((radio) => radio.setAttribute("aria-invalid", "true"));
  } else if (field) {
    field.setAttribute("aria-invalid", "true");
    field.classList.add("border-red-500", "focus:border-red-500", "focus:ring-red-200");
  }

  return false;
}

function clearFieldError(form, fieldName) {
  const errorElement = form.querySelector(`#${fieldName}-error`);
  const field = form.elements[fieldName];

  if (errorElement) {
    errorElement.textContent = "";
    errorElement.classList.add("hidden");
    errorElement.removeAttribute("role");
  }

  if (field instanceof RadioNodeList) {
    const radios = form.querySelectorAll(`input[name="${fieldName}"]`);
    radios.forEach((radio) => radio.removeAttribute("aria-invalid"));
  } else if (field) {
    field.removeAttribute("aria-invalid");
    field.classList.remove("border-red-500", "focus:border-red-500", "focus:ring-red-200");
  }
}

function clearAllErrors(form) {
  Object.keys(FIELD_ERRORS)
    .concat(["patient_id", "health_concern"])
    .forEach((fieldName) => clearFieldError(form, fieldName));
}

function getSelectedRadioValue(form, name) {
  const selected = form.querySelector(`input[name="${name}"]:checked`);
  return selected ? selected.value : "";
}

function isValidDateOfBirth(value) {
  if (!value) {
    return false;
  }

  const dob = new Date(`${value}T00:00:00`);
  if (Number.isNaN(dob.getTime())) {
    return false;
  }

  const today = startOfDay(new Date());
  if (dob > today) {
    return false;
  }

  const age = getAgeFromDate(value);
  return Number.isFinite(age) && age >= 0 && age <= 120;
}

function getAgeFromDate(value) {
  if (!value) {
    return NaN;
  }
  const dob = new Date(`${value}T00:00:00`);
  if (Number.isNaN(dob.getTime())) {
    return NaN;
  }

  const today = startOfDay(new Date());
  let age = today.getFullYear() - dob.getFullYear();
  const monthDiff = today.getMonth() - dob.getMonth();
  if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < dob.getDate())) {
    age -= 1;
  }
  return age;
}

function isValidPreferredDate(value) {
  if (!value) {
    return false;
  }
  const preferredDate = new Date(`${value}T00:00:00`);
  if (Number.isNaN(preferredDate.getTime())) {
    return false;
  }

  const today = startOfDay(new Date());
  const minDate = getNextBusinessDay(today);
  const maxDate = new Date(today);
  maxDate.setDate(maxDate.getDate() + 60);

  return preferredDate >= minDate && preferredDate <= maxDate;
}

function getNextBusinessDay(fromDate) {
  const candidate = new Date(fromDate);
  candidate.setDate(candidate.getDate() + 1);

  while (candidate.getDay() === 0 || candidate.getDay() === 6) {
    candidate.setDate(candidate.getDate() + 1);
  }

  return startOfDay(candidate);
}

function startOfDay(date) {
  const normalized = new Date(date);
  normalized.setHours(0, 0, 0, 0);
  return normalized;
}
