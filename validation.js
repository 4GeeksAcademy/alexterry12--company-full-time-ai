function updateClinicWarning() {
  const clinic = document.querySelector("#preferred_clinic")?.value;
  const time = document.querySelector("#preferred_time")?.value;
  const warning = document.querySelector("#clinic-warning");
  if (!warning) {
    return;
  }

  if (!clinic || !time || time !== "Evening (5pm-8pm)") {
    warning.classList.add("hidden");
    warning.textContent = "";
    return;
  }

  const closingHour = clinicClosingHour[clinic];
  if (typeof closingHour === "number" && closingHour < 20) {
    warning.textContent = t("clinic_warning");
    warning.classList.remove("hidden");
  } else {
    warning.classList.add("hidden");
    warning.textContent = "";
  }
}

