const NAME_REGEX = /^[A-Za-zÁÉÍÓÚáéíóúÑñÜü\s]{2,50}$/;
const PHONE_REGEX = /^\+\d{1,4}[\d\s-]{6,20}$/;
const MEMBER_ID_REGEX = /^[A-Za-z0-9]{6,20}$/;
const PATIENT_ID_REGEX = /^HC-[A-Za-z0-9]{6}$/;

let currentLanguage = "en";

document.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem("healthcore_language");
  currentLanguage = savedLanguage === "es" ? "es" : "en";

  setupLanguageSwitcher();
  applyLanguage(currentLanguage);

  const form = document.querySelector("#patient-enquiry-form");
  if (form) {
    setupFormValidation(form);
  }
});

function t(key, vars = {}) {
  const text = translations[currentLanguage][key] || translations.en[key] || key;
  return Object.keys(vars).reduce((acc, varKey) => acc.replace(`{${varKey}}`, String(vars[varKey])), text);
}

function setupLanguageSwitcher() {
  const buttons = document.querySelectorAll(".language-btn");
  buttons.forEach((btn) => {
    btn.addEventListener("click", () => {
      const selectedLanguage = btn.getAttribute("data-set-language");
      if (!selectedLanguage || selectedLanguage === currentLanguage) {
        return;
      }
      currentLanguage = selectedLanguage;
      localStorage.setItem("healthcore_language", currentLanguage);
      applyLanguage(currentLanguage);

      const form = document.querySelector("#patient-enquiry-form");
      if (form) {
        updateCounter();
        updateClinicWarning();
      }
    });
  });
}

function applyLanguage(languageCode) {
  document.documentElement.lang = languageCode;

  const keyedElements = document.querySelectorAll("[data-i18n]");
  keyedElements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    element.textContent = t(key);
  });

  const placeholderElements = document.querySelectorAll("[data-i18n-placeholder]");
  placeholderElements.forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.setAttribute("placeholder", t(key));
  });

  const buttons = document.querySelectorAll(".language-btn");
  buttons.forEach((btn) => {
    const isActive = btn.getAttribute("data-set-language") === languageCode;
    btn.classList.toggle("bg-brand-700", isActive);
    btn.classList.toggle("text-white", isActive);
    btn.classList.toggle("border-brand-700", isActive);
    btn.classList.toggle("text-slate-700", !isActive);
    btn.setAttribute("aria-pressed", String(isActive));
  });

  const successBox = document.querySelector("#form-status");
  if (successBox && !successBox.classList.contains("hidden")) {
    setSuccessMessage(successBox);
  }

  const form = document.querySelector("#patient-enquiry-form");
  if (!form) {
    return;
  }

  Object.keys(FIELD_ERRORS).forEach((fieldName) => {
    const errorElement = document.querySelector(`#${fieldName}-error`);
    if (errorElement && !errorElement.classList.contains("hidden")) {
      const errorKey = FIELD_ERRORS[fieldName];
      if (fieldName === "health_concern") {
        const textarea = form.elements.health_concern;
        const remaining = Math.max(0, 20 - (textarea.value.trim().length || 0));
        errorElement.textContent = t(errorKey, { x: remaining });
      } else {
        errorElement.textContent = t(errorKey);
      }
    }
  });
}

function setupFormValidation(form) {
  const insuranceRadios = form.querySelectorAll('input[name="has_insurance"]');
  const newPatientRadios = form.querySelectorAll('input[name="new_patient"]');

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const isValid = validateForm(form);
    if (!isValid) {
      const firstError = form.querySelector("[aria-invalid='true']");
      if (firstError) {
        firstError.focus();
      }
      return;
    }

    clearAllErrors(form);
    const successBox = form.querySelector("#form-status");
    if (successBox) {
      setSuccessMessage(successBox);
      successBox.classList.remove("hidden");
      successBox.scrollIntoView({ behavior: "smooth", block: "nearest" });
    }
    form.reset();
    toggleInsuranceFields(form);
    togglePatientIdField(form);
    updateCounter();
    updateClinicWarning();
  });

  form.addEventListener("reset", () => {
    window.setTimeout(() => {
      clearAllErrors(form);
      toggleInsuranceFields(form);
      togglePatientIdField(form);
      updateCounter();
      updateClinicWarning();
      const successBox = form.querySelector("#form-status");
      if (successBox) {
        successBox.classList.add("hidden");
      }
    }, 0);
  });

  const textAndSelectFields = [
    "first_name",
    "last_name",
    "date_of_birth",
    "email",
    "phone",
    "preferred_language",
    "preferred_clinic",
    "preferred_date",
    "preferred_time",
    "service_type",
    "insurance_provider",
    "insurance_member_id",
    "patient_id"
  ];

  textAndSelectFields.forEach((fieldName) => {
    const field = form.elements[fieldName];
    if (!field) {
      return;
    }
    field.addEventListener("blur", () => validateField(form, fieldName));
    field.addEventListener("input", () => validateField(form, fieldName));
    field.addEventListener("change", () => {
      validateField(form, fieldName);
      if (fieldName === "preferred_clinic" || fieldName === "preferred_time") {
        updateClinicWarning();
      }
    });
  });

  insuranceRadios.forEach((radio) => {
    radio.addEventListener("change", () => {
      toggleInsuranceFields(form);
      validateField(form, "has_insurance");
    });
  });

  newPatientRadios.forEach((radio) => {
    radio.addEventListener("change", () => {
      togglePatientIdField(form);
      validateField(form, "new_patient");
    });
  });

  const healthConcern = form.elements.health_concern;
  if (healthConcern) {
    healthConcern.addEventListener("input", () => {
      updateCounter();
      validateField(form, "health_concern");
    });
    healthConcern.addEventListener("blur", () => validateField(form, "health_concern"));
  }

  const consent = form.elements.contact_consent;
  if (consent) {
    consent.addEventListener("change", () => validateField(form, "contact_consent"));
  }

  toggleInsuranceFields(form);
  togglePatientIdField(form);
  updateCounter();
  updateClinicWarning();
}

function setSuccessMessage(successBox) {
  successBox.innerHTML = `
    <h3 class="text-lg font-extrabold">${t("success_title")}</h3>
    <p class="mt-2">${t("success_p1")}</p>
    <p class="mt-2">${t("success_p2")}</p>
    <p class="mt-2 font-semibold">${t("success_p3")}</p>
  `;
}

function validateForm(form) {
  const fieldsToValidate = [
    "first_name",
    "last_name",
    "date_of_birth",
    "email",
    "phone",
    "preferred_language",
    "preferred_clinic",
    "preferred_date",
    "preferred_time",
    "service_type",
    "new_patient",
    "has_insurance",
    "insurance_provider",
    "insurance_member_id",
    "patient_id",
    "health_concern",
    "contact_consent"
  ];

  return fieldsToValidate.reduce((acc, fieldName) => validateField(form, fieldName) && acc, true);
}

function validateField(form, fieldName) {
  switch (fieldName) {
    case "first_name": {
      const value = form.elements.first_name.value.trim();
      return setFieldResult(
        form,
        "first_name",
        NAME_REGEX.test(value),
        t("error_first_name")
      );
    }
    case "last_name": {
      const value = form.elements.last_name.value.trim();
      return setFieldResult(form, "last_name", NAME_REGEX.test(value), t("error_last_name"));
    }
    case "date_of_birth": {
      const value = form.elements.date_of_birth.value;
      const validDob = isValidDateOfBirth(value);
      const result = setFieldResult(form, "date_of_birth", validDob, t("error_dob"));
      if (form.elements.service_type.value === "Paediatric Care") {
        validateField(form, "service_type");
      }
      return result;
    }
    case "email": {
      const value = form.elements.email.value.trim();
      const isValid = /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value);
      return setFieldResult(form, "email", isValid, t("error_email"));
    }
    case "phone": {
      const value = form.elements.phone.value.trim();
      return setFieldResult(form, "phone", PHONE_REGEX.test(value), t("error_phone"));
    }
    case "preferred_language": {
      const value = form.elements.preferred_language.value;
      return setFieldResult(form, "preferred_language", Boolean(value), t("error_preferred_language"));
    }
    case "preferred_clinic": {
      const value = form.elements.preferred_clinic.value;
      const valid = Boolean(value);
      updateClinicWarning();
      return setFieldResult(form, "preferred_clinic", valid, t("error_preferred_clinic"));
    }
    case "preferred_date": {
      const value = form.elements.preferred_date.value;
      return setFieldResult(form, "preferred_date", isValidPreferredDate(value), t("error_preferred_date"));
    }
    case "preferred_time": {
      const value = form.elements.preferred_time.value;
      const valid = Boolean(value);
      updateClinicWarning();
      return setFieldResult(form, "preferred_time", valid, t("error_preferred_time"));
    }
    case "service_type": {
      const service = form.elements.service_type.value;
      if (!service) {
        return setFieldResult(form, "service_type", false, t("error_service_type"));
      }

      if (service === "Paediatric Care") {
        const dob = form.elements.date_of_birth.value;
        const age = getAgeFromDate(dob);
        const isUnder18 = Number.isFinite(age) && age < 18;
        if (!isUnder18) {
          return setFieldResult(form, "service_type", false, t("error_service_paediatric"));
        }
      }

      return setFieldResult(form, "service_type", true, "");
    }
    case "new_patient": {
      const selected = getSelectedRadioValue(form, "new_patient");
      const valid = selected === "yes" || selected === "no";
      return setFieldResult(form, "new_patient", valid, t("error_new_patient"));
    }
    case "has_insurance": {
      const selected = getSelectedRadioValue(form, "has_insurance");
      const valid = selected === "yes" || selected === "no";
      const baseValid = setFieldResult(form, "has_insurance", valid, t("error_has_insurance"));
      if (selected === "yes") {
        const providerValid = validateField(form, "insurance_provider");
        const memberValid = validateField(form, "insurance_member_id");
        return baseValid && providerValid && memberValid;
      }
      return baseValid;
    }
    case "insurance_provider": {
      const selected = getSelectedRadioValue(form, "has_insurance");
      if (selected !== "yes") {
        clearFieldError(form, "insurance_provider");
        return true;
      }
      const value = form.elements.insurance_provider.value.trim();
      const valid = value.length > 0 && value.length <= 100;
      return setFieldResult(form, "insurance_provider", valid, t("error_insurance_provider"));
    }
    case "insurance_member_id": {
      const selected = getSelectedRadioValue(form, "has_insurance");
      if (selected !== "yes") {
        clearFieldError(form, "insurance_member_id");
        return true;
      }
      const value = form.elements.insurance_member_id.value.trim();
      return setFieldResult(form, "insurance_member_id", MEMBER_ID_REGEX.test(value), t("error_member_id"));
    }
    case "patient_id": {
      const selected = getSelectedRadioValue(form, "new_patient");
      const value = form.elements.patient_id.value.trim();
      if (selected !== "no" || value.length === 0) {
        clearFieldError(form, "patient_id");
        return true;
      }
      return setFieldResult(form, "patient_id", PATIENT_ID_REGEX.test(value), t("error_patient_id"));
    }
    case "health_concern": {
      const value = form.elements.health_concern.value.trim();
      const valid = value.length >= 20 && value.length <= 500;
      const remaining = Math.max(0, 20 - value.length);
      return setFieldResult(form, "health_concern", valid, t("error_health_concern", { x: remaining }));
    }
    case "contact_consent": {
      const checked = form.elements.contact_consent.checked;
      return setFieldResult(form, "contact_consent", checked, t("error_consent"));
    }
    default:
      return true;
  }
}

